public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello\\Kg coding\n..");
        System.out.println("Hello\nKg coding");
        System.out.println("Hello\tKg coding");
        System.out.println("Hello\'Kg coding");
        System.out.println("Hello\"Kg coding");
        System.out.println("Hello\bKg coding");

    }
}
